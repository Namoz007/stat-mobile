abstract class AcceptanceResponseEntity {
  int bachelorCount;
  int mastersDegreeCount;
  int academicLyceumCount;
  int secondExpertCount;
  int readingTransferredCount;

  AcceptanceResponseEntity({
    required this.bachelorCount,
    required this.mastersDegreeCount,
    required this.academicLyceumCount,
    required this.secondExpertCount,
    required this.readingTransferredCount,
  });
}
