abstract class ProfessionalEducationFormEntity {
  int day_time;
  int external;
  int evening;
  int dual;
  int education_type;
  bool empty;

  ProfessionalEducationFormEntity({
    required this.day_time,
    required this.external,
    required this.evening,
    required this.dual,
    required this.education_type,
    required this.empty,
  });

}
